package com.github.regyl.model;

/**
 * Provides information about scope.
 */
public interface ScopeDetails {
    
    /**
     * Returns the scope as string.
     *
     * @return the scope
     */
    String getScope();
    
    /**
     * Returns the provider as string.
     *
     * @return the provider
     */
    String getProvider();
}
